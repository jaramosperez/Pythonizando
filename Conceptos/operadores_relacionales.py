# SIRVEN PARA VERIFICAR LA AUTENTICIDAD DE UNA OPERACION O COMPARACION

# IGUAL QUE
print(3 == 2)
# False

# DISTINDO DE
print(3 != 2)
# True

# MAYOR QUE
print(3 > 2)
# True

# MENOR QUE
print(3 < 2)
# False

# MAYOR O IGUAL QUE
print(3 >= 2)
# True

# MENOR O IGUAL QUE
print(3 <= 2)
# False

# USO DE VARIABLES
a = 10
b = 5
